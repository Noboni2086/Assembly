.MODEL SMALL
.STACK 100H
.DATA
A DB 'HELLO$'
B DB 'BESTS$'
C DB 'FRIENDS$'
D EQU 1
E EQU 2
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX 

MOV CX,5  

AA:
LEA DX,C
MOV AH,9
INT 21H 

MOV AH,2
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H 

DEC CX
CMP CX,3
JG AA


BB:
LEA DX,B
MOV AH,9
INT 21H 

MOV AH,2
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H 


LEA DX,A
MOV AH,9
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN